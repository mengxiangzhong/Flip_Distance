package flip_distance_all_second;


public class Triangle {
	int a,b,c;
	Triangle t1,t2,t3;
	Triangle(){
		a = 0;
		b = 0;
		c = 0;
		t1 = null;
		t2 = null;
		t3 = null;
	}
	Triangle(int x, int y, int z){
		a = x;
		b = y;
		c = z;
		t1 = null;
		t2 = null;
		t3 = null;
	}
}
